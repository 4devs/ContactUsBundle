<?php

namespace FDevs\ContactUsBundle\Model;

trait EmailTrait
{
    /** @var string */
    protected $email;

    /**
     * Set email
     *
     * @param string $email
     *
     * @return self
     */
    public function setEmail($email)
    {
        $this->email = $email;

        return $this;
    }

    /**
     * Get email
     *
     * @return string $email
     */
    public function getEmail()
    {
        return $this->email;
    }
}
